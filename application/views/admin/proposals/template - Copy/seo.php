        <div class="seo-overview">
            <p>
                <strong class="custom-color">OVERVIEW</strong>
                <span>-</span>
                SEO is the term given to the process of helping the search engines (like Google, Bing, and Yahoo!) figure out what your website is about, and how relevant it is to the search terms (called “keywords”) entered by users.  There are more than 200 factors that play into how the search engines decide that one website is more or less relevant than others.  While many of the factors used in the search engines' ranking algorithms are not disclosed, there's a wealth of verified publications discussing the known factors. Two of the biggest areas of concentration are called on-site optimization and off-site optimization.
                <ul class="custom-list-none">
                    <li>
                        <p>
                            <span class="font-weight-bold">On-site optimization </span>
                            refers to the coding, meta data, and content that make up your website.  Once on-site optimization is completed, it rarely needs any changing depending on the strategy applied.
                        </p>
                    </li>
                    <li>
                        <p>
                            <span class="font-weight-bold">Off-site optimization </span>
                            refers to the ongoing creation of content throughout the internet that creates organic traffic and links to your website (called “backlinks”). Every link and piece of content is like a vote cast for your website, telling the search engines that your site is more popular, trustworthy, and relevant than the other websites.  There are many ways links are built, such as guest blogging, directory listings, post commenting, article writing, media syndication, etc.
                        </p>
                    </li>
                </ul>
                Success in search engines is greatly dependent upon the geographical reach desired, the keywords pursued, and the amount of activity & effort that, compared to your competitors, you put into content marketing, social media.  Like a race, if you are the only one running, you are guaranteed first place without any effort.  However, the more competitors you have participating in the race, the harder you will have to work at achieving first place.
            </p>
