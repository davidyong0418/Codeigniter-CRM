                <p>
                    <strong class="custom-color">SEARCH ENGINE ADVERTISING (SEA)</strong>
                    <span class="custom-color">-</span>
                    This platform allows advertisers to bid for ad placement in a search engine whenever someone searches on a keyword that is related to their business offering.  This user is actively searching for a product or service.  Every time the ad is clicked, sending a visitor to the client’s website, the client has to pay the search engine a small fee.  If the ad is shown, but not clicked, the client pays nothing.
                </p>
