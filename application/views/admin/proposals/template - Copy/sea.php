                    <strong class="custom-color">OVERVIEW</strong>
                    <span>-</span>
                    One of the most common digital advertising models is PPC, which stands for pay-per-click.  PPC is a model of internet marketing in which advertisers pay a fee each time one of their ads is clicked. It works quickly to buy visits to client’s site, rather than attempting to “earn” those visits organically.  PPC is encompassing of many forms of advertising such text ads, banner ads, video ads, targeted ads, and retargeting ads (ads shown to people that have previously engaged with you).  The two primary platforms for managed ads are the search engines (such as Google, Bing, and Yahoo) and social media (such as Facebook, LinkedIn, and Pinterest).
